# Primeira parte: iterar sobre a string "Olá, laço for."
texto = 'Olá, laço for.'

for item in texto:
    print('Caractere:', item)

# Segunda parte: iterar sobre um intervalo de 1 a 10
for numero in range(1, 11):  # range(1, 11) gera números de 1 a 10
    print('Número do intervalo: ' + str(numero))  # Convertendo número para string